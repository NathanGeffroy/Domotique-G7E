			<h2 id="Gerer_mon_profil">Gérer mon profil</h2>
				<ul>
					<h3 id="2.1">Modifier mon profil</h3>
					<p>Si vous souhaitez modifier vos paramètres personnels, sur la page <a href="index.php?cible=monProfil">Mon Profil</a>, sélectionnez le bouton "Modifier mon profil" et remplissez les formulaires que vous souhaitez modifier. Les formulaires n'ayant pas été modifiés ne seront pas pris en compte et vos informations seront inchangées.</p><br/>
					
					<h3 id="2.2">Ajouter un utilisateur à mon domicile</h3>
					<p>Vous pouvez ajouter un utilisateur déjà inscrit sur Domisep à votre domicile. Pour cela, sur la page <a href="index.php?cible=monProfil">Mon Profil</a> entrez les informations nécessaires dans le cadre "Ajouter un utilisateur".</p><br/>
				</ul>



			<h2 id="Gerer_ma_maison">Gérer ma maison</h2>
				<ul>
					<h3 id="3.1">Ajouter une pièce</h3>
					<p>Rendez-vous sur la page <a href="index.php?cible=monDomicile">Mon Domicile</a> afin de contrôler votre maison. Pour ajouter une pièce, cliquez, sur le menu de gauche, "Ajouter une pièce" et entrez le nom de la pièce que vous souhaitez ajouter. Un fenêtre apparaîtra sur votre écran, confirmant l'ajout de la pièce.</p><br/>

					<h3 id="3.2">Ajouter un capteur</h3>
					<p>Toujours sur <a href="index.php?cible=monDomicile">Mon Domicile</a>, cliquez sur le bouton "ajouter un capteur" du menu de gauche. Remplissez les paramètres demandés tels que le type de capteur à ajouter et l'endroit voulu. Un capteur apparaîtra à l'endroit sélectionné.</p><br/>
					
					<h3 id="3.3">Informations d'une pièce ou d'un capteur</h3>
					<p>Afin d'accéder aux données des capteurs, cliquez dessus et agissez de la même manière pour une pièce. En effet, en sélectionnant une salle, les informations associées s'afficheront ainsi que les capteurs présent à cet endroit.</p><br/>
					
					<h3 id="3.4">Afficher mes capteurs par type ou par pièce</h3>
					<p>Sélectionnez l'affichage des capteurs par type afin de faire apparaître tous les types de capteurs que vous possédez dans votre domicile. Ensuite, vous pouvez les sélectionner pour visualiser les pièces dans lesquels ils sont présents.</p>
					<p>L'affichage par pièce est sélectionné par défaut, cependant, apres avoir choisi un autre type d'affichage, vous pouvez le retrouver quand bon vous semble en choisissant "Par pièce".</p><br/>
				</ul>



